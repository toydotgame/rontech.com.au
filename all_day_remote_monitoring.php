The GSM interface unit constantly monitors your standard telephone exchange line and switches to the GSM cellular network in the even of a line failure being detected.  Changes of status of your intruder alarm system will then be transmitted to our central monitoring station via the GSM cellular network until your standard telephone exchange line is reinstated.  <br /><br />
This type of communication format is recommended for <strong>"LOW RISK"</strong> security applications.  <br /><br />

The alternatives are <a href="#securitel" class="links" title="Securitel - High risk security applications from Rontech Securities">Securitel&reg;</a> or <a href="#multipath" class="links" title="Multi Path (IP, GPRS, PSTN) - High risk security applications from Rontech ">Multi Path (IP, GPRS, PSTN)</a>.<br /><br />

<a name="multipath"></a><h3 class="sub_body_text">Multi Path (IP, GPRS, PSTN)</h3>

This format utilises a number of communication paths to transmit changes in status of your Intruder Alarm system to our Central Monitoring Station such as:<br /><br />
<ul>
	<li>IP (ADSL) communication via a connection to the public IP or VPN (Virtual Private Network) service.</li>
	<li>GPRS communication utilises a polled Radio System which transmits via Rontech Security's VPN network. </li>
	<li>PSTN communication incorporates an automatic Digital Communicating device via your existing standard telephone exchange line.  </li>
</ul>
This type of communication format is recommended for <strong>"HIGH RISK"</strong> security applications.<br /><br />

 
<a name="securitel"></a><h3 class="sub_body_text">Securitel&reg;</h3>

This format incorporates a Telstra Securitel&reg; Interface, which transmits changes of alarm status to our Central Monitoring Station via Telstra Australia's Securitel&reg; Network. <br /><br />

Securitel&reg; is not available in all areas; please check with our sales representative for availability of this service in your area.<br /><br />

This type of communication format is recommended  for <strong>"HIGH RISK"</strong>security  applications.<br /><br />

