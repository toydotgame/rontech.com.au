					<div class="body_text">	<br />
					 Today, access control systems are finding many new applications.  They are being used to monitor and control access and progress to areas such as commercial buildings, hotels/motels, elevators, computer rooms, car parks, hazardous areas, pharmacy storage rooms just to name a few.<br />
					 <br />

Access control systems can range from small to extremely large systems involving up to ten thousand users.  One of the primary considerations affecting the selection of an access control system is to determine the number of individuals who require access to each area and their access schedule.<br /><br />

Computer managed access control systems can be used not only to control access to areas, but also to control access to plant and equipment. As a result, only authorised individuals would be able to physically operate the controlled equipment.<br /><br />

The actual type of access control system chosen depends on the level of security required.  A suitable system for one facility might not be suitable for another.<br /><br />

